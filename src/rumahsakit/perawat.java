//Codingan untuk tampil data
public void TampilData() {
   Object[] baris = {"id_perawat","nama_perawat","tempat_lahir","tanggal_lahir","jenis_kelamin","no_hp","alamat"};

        model = new DefaultTableModel(null, baris);
        Tabel.setModel(model);
        Connection connection = new Koneksi().getConnection();
        try {
            String sql = "select * from Perawat order by id_perawat asc";
            java.sql.Statement statement = connection.createStatement();
            java.sql.ResultSet hasil = statement.executeQuery(sql);
            while (hasil.next()){
                String idperawat = hasil.getString("id_perawat");
                String perawat = hasil.getString("nama_perawat");
                String TmpLahir = hasil.getString("tempat_lahir");
                String TglLahir = hasil.getString("tanggal_lahir");
                String JnsKelamin = hasil.getString("jenis_kelamin");
                String No_HP = hasil.getString("no_hp");
                String Alamat = hasil.getString("alamat");
                String[] data = {idperawat, perawat, TmpLahir, TglLahir, JnsKelamin, No_HP, Alamat};
                model.addRow(data);
            }
            
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, "Menampilkan data Gagal", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            JOptionPane.showMessageDialog(null, "Menampilkan data Gagal" + e);
        }
}


void simpandata(){
    //Deklarasi variabel
    String idperawat = id_perawat.getText(); //nama text field di form
    String perawat = nama_perawat.getText();
    String TmpLahir = tempat_lahir.getText();
    String TglLahir = new SimpleDateFormat("yyyy-MM-dd").format(tanggal_lahir.getDate());
    String JnsKelamin = jenis_kelamin.getSelectedItem().toString();
    String No_HP = no_hp.getText();
    String Alamat = alamat.getText();
    
    
    //Koneksi terhadap database
    Connection connection = new Koneksi().getConnection();
    
    
    //Proses penyimpanan
    try{
        String sql = "insert into perawat values(?,?,?,?,?,?,?)";
        PreparedStatement statement = connection.prepareStatement(sql);
        //connection.prepareStatement(sql);
        
        statement.setString(1, idperawat);
        statement.setString(2, perawat);
        statement.setString(3, TmpLahir);
        statement.setString(4, TglLahir);
        statement.setString(5, JnsKelamin);
        statement.setString(6, No_HP);
        statement.setString(7, Alamat);
        
        statement.executeUpdate();
        
        JOptionPane.showMessageDialog(null, "DATA BERHASIL DISIMPAN","informasi",JOptionPane.INFORMATION_MESSAGE);
        
    }catch (HeadlessException | SQLException e) {
      
        JOptionPane.showMessageDialog(null, "DATA GAGAL DISIMPAN \n"); //Menampilkan laporan data gagal disimpan
    }
}

