//Codingan untuk tampil data
public void TampilData() {
    Object[] baris = {"id_dokter","nama_dokter","alamat","tempat_lahir","tanggal_lahir","jenis_kelamin","no_hp","spesialis"};

        model = new DefaultTableModel(null, baris);
        Tabel.setModel(model);
        Connection connection = new Koneksi().getConnection();
        try {
            String sql = "select * from Dokter order by id_dokter asc";
            java.sql.Statement statement = connection.createStatement();
            java.sql.ResultSet hasil = statement.executeQuery(sql);
            while (hasil.next()){
                String iddokter = hasil.getString("id_dokter");
                String namadokter = hasil.getString("nama_dokter");
                String Alamat = hasil.getString("alamat");
                String TmpLahir = hasil.getString("tempat_lahir");
                String TglLahir = hasil.getString("tanggal_lahir");
                String JnsKelamin = hasil.getString("jenis_kelamin");
                String No_HP = hasil.getString("no_hp");
                String Spesialis = hasil.getString("spesialis");
                String[] data = {iddokter, namadokter, Alamat, TmpLahir, TglLahir, JnsKelamin, No_HP, Spesialis};
                model.addRow(data);
            }
            
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, "Menampilkan data Gagal", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            JOptionPane.showMessageDialog(null, "Menampilkan data Gagal" + e);
        }
}

