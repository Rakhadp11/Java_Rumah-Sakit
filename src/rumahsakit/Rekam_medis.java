void simpandata(){
    //Deklarasi variabel
    String nonrm = no_rekam_medis.getText();
    String nopasien = no_pasien.getText();
    String dokter = id_dokter.getText();
    String perawat = id_perawat.getText();
    String ruang = kode_ruangan.getText();
    String keluh = keluhan.getText();
    String sakit = nama_penyakit.getText();
    
    
    //Koneksi terhadap database
    Connection connection = new Koneksi().getConnection();
    
    
    //Proses penyimpanan
    try{
        String sql = "insert into Rekam_medis values(?,?,?,?,?,?,?)";
        PreparedStatement statement = connection.prepareStatement(sql);
        //connection.prepareStatement(sql);
        
        statement.setString(1, nonrm);
        statement.setString(2, nopasien);
        statement.setString(3, dokter);
        statement.setString(4, perawat);
        statement.setString(5, ruang);
        statement.setString(6, keluh);
        statement.setString(7, sakit);
        
        statement.executeUpdate();
        
        JOptionPane.showMessageDialog(null, "DATA BERHASIL DISIMPAN","informasi",JOptionPane.INFORMATION_MESSAGE);
        
    }catch (HeadlessException | SQLException e) {
     
        JOptionPane.showMessageDialog(null, "DATA GAGAL DISIMPAN \n"); //Menampilkan laporan data gagal disimpan
    }
}

