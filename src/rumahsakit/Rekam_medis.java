//Codingan untuk tampil data
public void TampilData() {
    Object[] baris = {"no_rekam_medis","no_pasien","id_dokter","id_perawat","kode_ruangan","keluhan","nama_penyakit"};

        model = new DefaultTableModel(null, baris);
        Tabel.setModel(model);
        Connection connection = new Koneksi().getConnection();
        try {
            String sql = "select * from Rekam_medis order by no_rekam_medis asc";
            java.sql.Statement statement = connection.createStatement();
            java.sql.ResultSet hasil = statement.executeQuery(sql);
            while (hasil.next()){
                String nonrm = hasil.getString("no_rekam_medis");
                String nopasien = hasil.getString("no_pasien");
                String dokter = hasil.getString("id_dokter");
                String perawat = hasil.getString("id_perawat");
                String ruang = hasil.getString("kode_ruangan");
                String keluh = hasil.getString("keluhan");
                String sakit = hasil.getString("nama_penyakit");
                String[] data = {nonrm, nopasien, dokter, perawat, ruang, keluh, sakit};
                model.addRow(data);
            }
            
        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, "Menampilkan data Gagal", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            JOptionPane.showMessageDialog(null, "Menampilkan data Gagal" + e);
        }
}

